const content = document.getElementById('content');
const nav = document.getElementById('genre-nav');

Object.keys(mangaData).forEach(genre => {
    // Навигация
    const btn = document.createElement('button');
    btn.className = 'nav-btn';
    btn.innerText = genre;
    btn.onclick = () => document.getElementById(genre).scrollIntoView({behavior: "smooth"});
    nav.appendChild(btn);

    // Секции
    const section = document.createElement('section');
    section.id = genre;
    section.className = 'genre-section';
    section.innerHTML = `<h2 class="genre-title">${genre}</h2>`;
    
    const grid = document.createElement('div');
    grid.className = 'grid';

    mangaData[genre].forEach(item => {
        grid.innerHTML += `
            <div class="card">
                <img src="${item.poster}" class="poster" alt="${item.name}" onerror="this.src='https://via.placeholder.com/300x400?text=No+Image'">
                <div class="info">
                    <a href="${item.link}" target="_blank" class="author-name">${item.name}</a>
                    <div class="desc">${item.desc}</div>
                </div>
            </div>
        `;
    });
    
    section.appendChild(grid);
    content.appendChild(section);
});