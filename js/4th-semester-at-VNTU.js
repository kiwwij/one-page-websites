function renderCard() {
    if (currentQuestions.length === 0) return;

    const item = currentQuestions[currentIndex];
    
    // Update content with delay for smooth flip
    setTimeout(() => {
        questionEl.textContent = item.q;
        
        // --- FIX: Simple text replacement or raw HTML ---
        // Якщо у тексті є source, додаємо його гарно
        let answerHTML = item.a;
        if (item.source) {
            answerHTML += `<br><br><span style="font-size:0.7em; color:#9ca3af;"> [Джерело: ${item.source}]</span>`;
        }
        
        answerContentEl.innerHTML = answerHTML;
        counterEl.textContent = `${currentIndex + 1} / ${currentQuestions.length}`;
        
        // Buttons
        prevBtn.disabled = currentIndex === 0;
        
        if (currentIndex === currentQuestions.length - 1) {
            nextBtn.textContent = '🔁 Спочатку';
        } else {
            nextBtn.textContent = 'Вперед ❯';
        }
    }, 200);
}

// Navigation
function nextQuestion() {
    if (currentIndex < currentQuestions.length - 1) {
        card.classList.remove('is-flipped');
        currentIndex++;
        setTimeout(renderCard, 300);
    } else {
        if (confirm("Це було останнє питання. Почати знову?")) {
            card.classList.remove('is-flipped');
            currentIndex = 0;
            if (isRandom) shuffleArray(currentQuestions);
            setTimeout(renderCard, 300);
        }
    }
}

function prevQuestion() {
    if (currentIndex > 0) {
        card.classList.remove('is-flipped');
        currentIndex--;
        setTimeout(renderCard, 300);
    }
}

// Helper: Shuffle
function shuffleArray(array) {
    for (let i = array.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [array[i], array[j]] = [array[j], array[i]];
    }
}